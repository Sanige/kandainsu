	<h2>Claim services</h2>
	<div class="page-content">
		<p>At Kanda international insurance brokers, we pride ourselves on the way we support our business and personal clients when they need to make a claim. we understand that even the smallest claim can be a worry and a hassle. That is why we are aways available for help. </p>
		<p>We have specially trained staff who will assist in your claims Management. We will have a fast turnaround on the claims submitted to us. Kanda international recognizes that our Client's insurance are realized when the insured event catastrophe occurs. Therefore, we are resourced to give you the maximum assistant possible at the time when you need us the most, claims time. Large claims can be managed in liaison with the appointed Loss Adjusters who are professionals in this area. Kanda International will also liaise closely with the Lawyers in resolving your claims at the most diligent and efficient manner. Remember that Kanda International will always act on behalf of the insured (Our Clients) not the insurers.</p>
		<p><b>It is obvious that in the event of an incident which will or may give rise to a claim, a procedure must be adopted for the proper and expeditious conduct of the claim. By carefully following this procedure, the impact on your business operations will be minimized.</b></p>
		<p>There are number of steps which must be taken following a loss:</p>
		<p>Report the incident to the Kanda International office that looks after your insurance program, by telephone, facsimile or email - wherever practical, within 24 hours of the incident.</p>
		<p>Regardless of whether or not the claim has been reported or a loss assessor appointed, you must immediately do whatever is necessary to prevent further loss of life or property damage.</p>
		<ul>
			<li>Whatever the circumstances of the incident, <b>DO NOT ADMIT LIABILITY</b> EVEN IF YOU THINK YOU ARE AT FAULT.</li>
			<li>Your insurer is entitled to deny a claim or pay a reduced amount if statements made by you or your employees prejudice the insurer's position.</li>
			<li>Report the Incident to your nearest police station and provide a statement.</li>
		</ul>
		<p>It is impossible to give guidelines for procedures in every claim, simply because of the nature of accidents:</p>
		<ul>
			<li>They cannot be predicted</li>
			<li>They do not follow set patterns</li>
		</ul>
		<p>However, the general procedures for various major classes of insurance detailed above, can be relied upon to cover most circumstances. For any other claim involving other classes of insurance, please always contact your Kanda Insurance Brokers office without delay.</p>
