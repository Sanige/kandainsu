	<h2>Premium Funding</h2>
	<div class="page-content">
		<p>We have facilities available with Banks and Finance Institutions where premium can be obtained at very competitive rates. The procedure is very simple and is free of collateral. </p>
		<p>Payment of insurance premiums annually in advance can place a financial strain on any business. In the current economic climate it is imperative that you optimise credit terms, allowing expense to be managed in the most efficient manner. </p>
		<p>Spreading the cost of insurance premiums through a structured monthly repayment program produces the following benefits:</p>
		<ul>
			<li>Preserves existing lines of credit for traditional working capital requirements</li>
			<li>Assists cash flow management</li>
			<li>Premiums are a business expense, thus interest costs are tax deductible</li>
			<li>Creates a separate line of credit</li>
			<li>Premium financiers require no physical security. Their security takes the form of a lien over the unexpired portion of your insurance premiums.</li>
		</ul>
